# Control Flow Techniques

# 1. Sequencing
# Executes statements in order, crucial for tasks like variable initialization.

# 2. Selection
# Chooses execution paths based on conditions using 'if' and 'else', enabling 
# dynamic responses to inputs.

# 3. Iteration
# Repeats code blocks until a condition is met, using loops like 'for' and 
# 'while'. Useful for collections or unknown repetitions.