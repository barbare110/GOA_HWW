print("Equality (==) Examples:")
print(5 == 5)  # True
print(10 == 7)  # False
print("Hi" == "Hi")  # True

print("Greater Than or Equal (>=) Examples:")
print(5 >= 5)  # True
print(6 >= 3)  # True
print(4 >= 8)  # False

print("Less Than or Equal (<=) Examples:")
print(2 <= 3)  # True
print(7 <= 7)  # True
print(9 <= 5)  # False